function vgLink (key) {
  var vglnk = {key: key};
  (function(d, t) {
    var s = d.createElement(t);
      s.type = 'text/javascript';
      s.async = true;
      s.src = '//cdn.viglink.com/api/vglnk.js';
    var r = d.getElementsByTagName(t)[0];
      r.parentNode.insertBefore(s, r);
  }(document, 'script'));
 
};